// Addition
function add(num1,num2){
    return num1+num2
}
var res=add(20,10)
console.log(res);

// Substraction

function sub(num1,num2){
    return num1-num2
}
var res=sub(20,10)
console.log(res);

// Multiplication
function mul(num1,num2){
    return num1*num2
}
var res=mul(20,10)
console.log(res);

// Division
function div(num1,num2){
    return num1/num2
}
var res=div(20,10)
console.log(res);

function cube(no){
    return no**3
}
var res=cube(5)
console.log(res);